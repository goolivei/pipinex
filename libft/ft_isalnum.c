#include "libft.h"

int	ft_isalnum(int str)
{
	if ((str >= '0' && str <= '9')
		|| (str >= 'a' && str <= 'z')
		|| (str >= 'A' && str <= 'Z'))
	{
		return (1);
	}
	return (0);
}
